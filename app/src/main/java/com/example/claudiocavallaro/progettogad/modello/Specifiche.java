public class Specifiche {

    private String so;
    private String dimensioni;
    private String peso;

    private String processore;
    private String ram;
    private String schermo;
    private String risoluzione;
    private String fotocamera;
    private String frontale;

    private String batteria;


    public String getSo() {
        return so;
    }

    public void setSo(String so) {
        this.so = so;
    }

    public String getDimensioni() {
        return dimensioni;
    }

    public void setDimensioni(String dimensioni) {
        this.dimensioni = dimensioni;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getProcessore() {
        return processore;
    }

    public void setProcessore(String processore) {
        this.processore = processore;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getSchermo() {
        return schermo;
    }

    public void setSchermo(String schermo) {
        this.schermo = schermo;
    }

    public String getRisoluzione() {
        return risoluzione;
    }

    public void setRisoluzione(String risoluzione) {
        this.risoluzione = risoluzione;
    }

    public String getFotocamera() {
        return fotocamera;
    }

    public void setFotocamera(String fotocamera) {
        this.fotocamera = fotocamera;
    }

    public String getFrontale() {
        return frontale;
    }

    public void setFrontale(String frontale) {
        this.frontale = frontale;
    }

    public String getBatteria() {
        return batteria;
    }

    public void setBatteria(String batteria) {
        this.batteria = batteria;
    }
}
